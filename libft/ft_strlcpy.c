#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	x;

	x = ft_strlen(src);
	i = 0;
	if (size != 0)
	{
		while (*src != '\0' && i < size -1)
		{
			*dest++ = *src++;
			i++;
		}
		*dest = '\0';
	}
	return (x);
}
